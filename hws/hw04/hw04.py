class VendingMachine:
    """A vending machine that vends some product for some price.

    >>> v = VendingMachine('candy', 10)
    >>> v.vend()
    'Nothing left to vend. Please restock.'
    >>> v.add_funds(15)
    'Nothing left to vend. Please restock. Here is your $15.'
    >>> v.restock(2)
    'Current candy stock: 2'
    >>> v.vend()
    'You must add $10 more funds.'
    >>> v.add_funds(7)
    'Current balance: $7'
    >>> v.vend()
    'You must add $3 more funds.'
    >>> v.add_funds(5)
    'Current balance: $12'
    >>> v.vend()
    'Here is your candy and $2 change.'
    >>> v.add_funds(10)
    'Current balance: $10'
    >>> v.vend()
    'Here is your candy.'
    >>> v.add_funds(15)
    'Nothing left to vend. Please restock. Here is your $15.'

    >>> w = VendingMachine('soda', 2)
    >>> w.restock(3)
    'Current soda stock: 3'
    >>> w.restock(3)
    'Current soda stock: 6'
    >>> w.add_funds(2)
    'Current balance: $2'
    >>> w.vend()
    'Here is your soda.'
    """
    "*** YOUR CODE HERE ***"
    
    def __init__(self, sort, price):
        self.sort = sort
        self.price = price
        self.amount = 0
        self.account = 0
        self.curr_account = 0
    
    def sell(self):
        self.amount -= 1
        self.curr_account = 0
        self.account += self.price

    def vend(self):
        if self.amount == 0:
            return 'Nothing left to vend. Please restock.'
        diff = self.price - self.curr_account
        if diff < 0:
            self.sell()
            ret = abs(diff)
            return f'Here is your candy and ${ret} change.'
        elif diff > 0:
            return f'You must add ${diff} more funds.'
        else:
            self.sell()
            return f'Here is your {self.sort}.'

    def add_funds(self, funds):
        self.curr_account += funds
        if self.amount == 0:
            refund = self.curr_account
            self.curr_account = 0
            return f'Nothing left to vend. Please restock. Here is your ${refund}.'
        return f'Current balance: ${self.curr_account}'
    
    def restock(self, amount):
        self.amount += amount
        return f'Current {self.sort} stock: {self.amount}'